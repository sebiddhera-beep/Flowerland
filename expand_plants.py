def norm_sci(s):
    s = unicodedata.normalize("NFKC", s).strip().lower()
    return re.sub(r"\s+", " ", s)


def norm_ko(s):
    return unicodedata.normalize("NFKC", s).strip()


def load_existing(src):
    with open(src, encoding="utf-8-sig") as f:
        rows = list(csv.DictReader(f))
    if not rows:
        raise SystemExit("원본이 비어 있습니다: " + src)
    need = {"pid", "category", "korean", "english_common", "scientific"}
    if not need.issubset(rows[0].keys()):
        raise SystemExit("원본 컬럼이 예상과 다릅니다: " + ",".join(rows[0].keys()))
    return rows


def build(src, target, outdir):
    existing = load_existing(src)
    used_sci = {norm_sci(r["scientific"]) for r in existing}
    used_ko = {norm_ko(r["korean"]) for r in existing}
    last_num = max(int(r["pid"][1:]) for r in existing)

    seen_sci, seen_ko = set(used_sci), set(used_ko)
    new_rows = []
    dup_exist = dup_inner = 0
    for cat, ko, en, sci in CANDIDATES:
        nk, ns = norm_ko(ko), norm_sci(sci)
        if ns in used_sci or nk in used_ko:
            dup_exist += 1; continue
        if ns in seen_sci or nk in seen_ko:
            dup_inner += 1; continue
        seen_sci.add(ns); seen_ko.add(nk)
        new_rows.append((cat, ko, en, sci))

    print("후보 %d개 · 기존중복 %d · 내부중복 %d · 사용가능 신규 %d"
          % (len(CANDIDATES), dup_exist, dup_inner, len(new_rows)))

    if len(new_rows) < target:
        print("⚠️ 신규 후보가 %d개로 목표 %d에 부족합니다. CANDIDATES를 보강하세요."
              % (len(new_rows), target))
        sys.exit(2)
    new_rows = new_rows[:target]

    fields = ["pid", "category", "korean", "english_common", "scientific"]
    numbered = []
    for i, (cat, ko, en, sci) in enumerate(new_rows):
        numbered.append({"pid": "P%d" % (last_num + 1 + i), "category": cat,
                         "korean": ko, "english_common": en, "scientific": sci})

    os.makedirs(outdir, exist_ok=True)
    p_new = os.path.join(outdir, "plants_new_%d.csv" % target)
    p_full = os.path.join(outdir, "plants_master_v2.csv")
    p_xlsx = os.path.join(outdir, "plants_master_v2_excel.csv")

    def dump(path, rows, bom=False):
        enc = "utf-8-sig" if bom else "utf-8"
        with open(path, "w", encoding=enc, newline="") as f:
            w = csv.DictWriter(f, fieldnames=fields); w.writeheader(); w.writerows(rows)

    old_slim = [{k: r[k] for k in fields} for r in existing]
    dump(p_new, numbered)
    dump(p_full, old_slim + numbered)
    dump(p_xlsx, old_slim + numbered, bom=True)

    allrows = old_slim + numbered
    pids = [r["pid"] for r in allrows]
    assert len(pids) == len(set(pids)), "PID 중복 발생"
    kos = [norm_ko(r["korean"]) for r in allrows]
    print("-" * 58)
    print("기존 %d + 신규 %d = 전체 %d" % (len(existing), len(numbered), len(allrows)))
    print("신규 PID: %s ~ %s" % (numbered[0]["pid"], numbered[-1]["pid"]))
    print("전체 한글명 고유 %d/%d (중복 %d)"
          % (len(set(kos)), len(kos), len(kos) - len(set(kos))))
    print("저장:")
    for p in (p_new, p_full, p_xlsx):
        print("   " + p)


def main():
    here = os.path.dirname(os.path.abspath(__file__))
    ap = argparse.ArgumentParser()
    ap.add_argument("--src", default=os.path.join(here, "plants_master.csv"),
                    help="원본 마스터 CSV 경로")
    ap.add_argument("--target", type=int, default=1000, help="생성할 신규 종 수")
    ap.add_argument("--outdir", default=here, help="출력 폴더")
    args = ap.parse_args()
    if not os.path.exists(args.src):
        raise SystemExit("원본을 찾을 수 없습니다: %s\n(--src 로 경로를 지정하세요)" % args.src)
    build(args.src, args.target, args.outdir)


if __name__ == "__main__":
    main()
